import multer from "multer";
import PDFParser from "pdf2json";
import fs from "fs";

export const config = {
  api: {
    bodyParser: false, // Needed to handle file uploads
  },
};

// Configure multer for file uploads
const upload = multer({ storage: multer.memoryStorage() });

// âœ… **Function to extract text from PDF**
const parsePDF = (pdfData) => {
  console.log("âœ… Extracting text from PDF...");
  let extractedText = [];

  pdfData.Pages.forEach((page) => {
    page.Texts.forEach((text) => {
      extractedText.push(decodeURIComponent(text.R[0].T));
    });
  });

  console.log("ğŸ“œ Extracted text:", extractedText);
  return extractedText;
};

// âœ… **Extract Metadata (Report Owner, Date, etc.)**
const extractMetadata = (extractedText) => {
  console.log("ğŸ” Extracting metadata...");
  let metadata = {};

  let metaIndex = extractedText.indexOf("Report Generated By  :");
  if (metaIndex !== -1) {
    metadata["Report Owner"] = extractedText[metaIndex + 1] || "";
  }

  let dateIndex = extractedText.indexOf("Generated On  :");
  if (dateIndex !== -1) {
    metadata["Generated Date"] = extractedText[dateIndex + 1] || "";
  }

  let departmentIndex = extractedText.indexOf("Department Name:");
  if (departmentIndex !== -1) {
    metadata["Department Name"] = extractedText[departmentIndex + 1] || "";
  }

  console.log("ğŸ“‹ Extracted Metadata:", metadata);
  return metadata;
};

// âœ… **Extract Table Data Correctly**
const extractTableData = (extractedText) => {
  console.log("ğŸ›  Extracting table headers and ticket data...");

  let headers = [
    "Ticket Id",
    "Site",
    "Created Time (Ticket)",
    "Priority (Ticket)",
    "Subject",
    "Contact Name",
    "Status (Ticket)",
  ];

  let dataStartIndex = extractedText.indexOf("Ticket Id");
  if (dataStartIndex === -1) {
    console.error("âŒ Table headers not found!");
    return { headers: [], records: [] };
  }

  let ticketData = [];
  let currentTicket = {};
  let priorityIndex = -1; // Store the index where priority is found

  console.log("ğŸ” Data extraction starts at index:", dataStartIndex);
  let i = dataStartIndex + headers.length;

  while (i < extractedText.length) {
    let line = extractedText[i];

    console.log(`ğŸ“ Processing Line [${i}]:`, line);

    // ğŸ›‘ **Detecting a new ticket**
    if (!isNaN(line) && headers.includes("Ticket Id")) {
      console.log("ğŸ“Œ Detected a new Ticket ID:", line);

      if (Object.keys(currentTicket).length > 0) {
        ticketData.push(currentTicket);
        console.log("âœ… Saved Ticket:", currentTicket);
      }

      // Reset ticket details
      currentTicket = {};
      priorityIndex = i + 3; // Store the index of "Priority (Ticket)"

      currentTicket["Ticket Id"] = line;
      currentTicket["Site"] = extractedText[i + 1] || "";
      currentTicket["Created Time (Ticket)"] = extractedText[i + 2] || "";
      currentTicket["Priority (Ticket)"] = extractedText[i + 3] || "";

      console.log("ğŸ¯ Extracted Ticket Fields:");
      console.log("  ğŸ“Œ Ticket ID:", currentTicket["Ticket Id"]);
      console.log("  ğŸ“Œ Site:", currentTicket["Site"]);
      console.log("  ğŸ“Œ Created Time:", currentTicket["Created Time (Ticket)"]);
      console.log("  ğŸ“Œ Priority:", currentTicket["Priority (Ticket)"]);

      i += 4; // Move index past known single-line values
      continue;
    }

    // ğŸŸ¢ **Detect "Status"**
    if (line === "Closed" || line === "Open") {
      console.log("ğŸŸ¢ Detected Status:", line);

      currentTicket["Status (Ticket)"] = line;
      let contactIndex = i - 1;
      let contactName = extractedText[contactIndex] || "";

      console.log("ğŸ” Checking Possible Contact Name:", contactName);

      // **Detect Contact Name**
      if (/^[A-Za-z\s]+$/.test(contactName) && contactName.length < 50) {
        currentTicket["Contact Name"] = contactName;
        console.log("âœ… Valid Contact Name:", contactName);
      } else {
        console.log("âŒ Invalid Contact Name. Assigning empty.");
        currentTicket["Contact Name"] = "";
      }

      // ğŸ”¥ **Use `.slice()` to extract the subject correctly**
      let subjectStartIndex = priorityIndex + 1;
      let subjectEndIndex = contactIndex;

      currentTicket["Subject"] = extractedText.slice(subjectStartIndex, subjectEndIndex).join(" ");
      console.log("ğŸ“ Final Subject Extracted:", currentTicket["Subject"]);

      i++;
      continue;
    }

    i++;
  }

  // Add the last ticket
  if (Object.keys(currentTicket).length > 0) {
    ticketData.push(currentTicket);
    console.log("âœ… Final Ticket Added:", currentTicket);
  }

  return { headers, records: ticketData };
};

// âœ… **Handle File Upload in Next.js API Route**
const handler = async (req, res) => {
  if (req.method !== "POST") {
    return res.status(405).json({ error: "Method Not Allowed" });
  }

  upload.single("pdf")(req, res, async (err) => {
    if (err) {
      return res.status(500).json({ error: "File upload error" });
    }

    if (!req.file) {
      return res.status(400).json({ error: "No file uploaded" });
    }

    try {
      console.log("ğŸ”„ Processing PDF...");
      let pdfParser = new PDFParser();
      pdfParser.parseBuffer(req.file.buffer);

      pdfParser.on("pdfParser_dataReady", (pdfData) => {
        console.log("âœ… PDF Parsing Completed!");

        let extractedText = parsePDF(pdfData);
        let metadata = extractMetadata(extractedText);
        let tableData = extractTableData(extractedText);

        console.log("ğŸ“¤ Sending Response to Frontend...");
        res.json({ success: true, metadata, tableData });
      });

      pdfParser.on("pdfParser_dataError", (err) => {
        console.error("âŒ Error Parsing PDF:", err);
        res.status(500).json({ error: "Error processing PDF" });
      });
    } catch (error) {
      console.error("âŒ Unexpected Error:", error);
      res.status(500).json({ error: "Internal Server Error" });
    }
  });
};

export default handler;
